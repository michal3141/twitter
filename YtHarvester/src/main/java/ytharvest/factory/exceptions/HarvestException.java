package ytharvest.factory.exceptions;


/**
 * Main exception of YtHarvester library. All other exceptions can be caught by a reference to
 * HarvestException.
 */
public class HarvestException extends Exception
{

	/** The Constant serialVersionUID. */
	private static final long serialVersionUID = 1L;


	/**
	 * Instantiates a new HarvestException.
	 */
	public HarvestException()
	{
		super();
	}


	/**
	 * Instantiates a new HarvestException, initializing exception's message.
	 * 
	 * @param msg
	 *            the exception's message.
	 */
	public HarvestException(String msg)
	{
		super(msg);
	}


	/**
	 * Instantiates a new HarvestException, initializing exception's cause.
	 * 
	 * @param cause
	 *            the exception's cause.
	 */
	public HarvestException(Throwable cause)
	{
		super(cause);
	}


	/**
	 * Instantiates a new HarvestException, initializing exception's message and cause.
	 * 
	 * @param message
	 *            the exception's message.
	 * @param cause
	 *            the exception's cause.
	 */
	public HarvestException(String message, Throwable cause)
	{
		super(message, cause);

	}


	/**
	 * Instantiates a new HarvestException, initializing exception's cause.
	 * 
	 * @param cause
	 *            the exception's cause.
	 */
	public HarvestException(Exception cause)
	{
		this((Throwable) cause);
	}

}
